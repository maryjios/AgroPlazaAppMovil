package com.example.agroplazaappmovil.ui.dashboard;

public class PublicacionesPerfil {
    String id;
    String foto;
    String titulo;
    String envio;
    String descuento;
    String precio;
    String descripcion;
    String unidad;
    String stock;
    public PublicacionesPerfil (String id, String foto, String titulo, String envio, String descuento, String precio, String descripcion, String unidad, String stock) {

        this.id = id;
        this.foto = foto;
        this.titulo = titulo;
        this.envio = envio;
        this.descuento = descuento;
        this.precio = precio;
        this.descripcion = descripcion;
        this.unidad = unidad;
        this.stock = stock;

    }
}
